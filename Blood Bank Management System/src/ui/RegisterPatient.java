    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
            con=DBClass.getConnection();
            
            if (txtPId.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter patient ID","Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            if (txtname.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter patient name","Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            
            if (txtAddress.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter address","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
           if (txtContact.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter contact no.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
         
            if (txtAge.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter age","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
             if (cmbGender.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select gender","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbBG.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select blood group","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
           
          
   Statement stmt;
       stmt= con.createStatement();
       
       String sql= "select email from client where email= '" + txtEmail.getText() + "'";
          pst=con.prepareStatement(sql);
          rs= pst.executeQuery();
          String sql3= "select id from client where id= '" + txtPId.getText() + "'";
          pst2=con.prepareStatement(sql3);
          rs2= pst2.executeQuery();
          if (rs.next()){
             JOptionPane.showMessageDialog(this,"Email is already used, Use a new Email.");
             //txtEmail.requestDefaultFocus();
             txtEmail.setText("");
          }
          else if (rs2.next()){
             JOptionPane.showMessageDialog(this,"ID is already used, Use a new ID.");
             //txtEmail.requestDefaultFocus();
             txtPId.setText("");
          }
          else{
            String sql2= "insert into Client(id,name,Email,Contact,Address,Age,Sex,BG)values('"+ txtPId.getText() + "','"+ txtname.getText() + "','"+ txtEmail.getText() + "','"+ txtContact.getText() + "','"+ txtAddress.getText() + "'," + txtAge.getText() + ",'"+ cmbGender.getSelectedItem().toString() + "','"+ cmbBG.getSelectedItem().toString() + "')";
            pst=con.prepareStatement(sql2);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Successfully Registered","Patient",JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);
pst.close();
          }
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        } catch (Exception ex) {
            Logger.getLogger(RegisterPatient.class.getName()).log(Level.SEVERE, null, ex);
        }
                     
    }//GEN-LAST:event_btnSaveActionPerformed

