    private void txtNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameKeyReleased
        // TODO add your handling code here:

        try {
            try {
                con = DBClass.getConnection();
            } catch (Exception ex) {
                Logger.getLogger(DonorSearchResult.class.getName()).log(Level.SEVERE, null, ex);
            }
            String sql = "select id as 'Patient ID',Name,Email,Contact,Address,Age,Sex,BG as'Blood Group',(select Date from issued where client.id=issued.PID order by Date desc LIMIT 1) as 'Last received' from client where id='"+txtName.getText()+"' order by id desc";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            tblPatient.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (SQLException ex) {
            Logger.getLogger(ViewBlood.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtNameKeyReleased

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            try {
                con = DBClass.getConnection();
            } catch (Exception ex) {
                Logger.getLogger(DonorSearchResult.class.getName()).log(Level.SEVERE, null, ex);
            }
            String sql = "select id as 'Patient ID',Name,Email,Contact,Address,Age,Sex,BG as'Blood Group',(select Date from issued where client.id=issued.PID order by Date desc LIMIT 1) as 'Last received' from client where id='"+txtName.getText()+"' order by id desc";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
            tblPatient.setModel(DbUtils.resultSetToTableModel(rs));
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Name Not Found! Try another one..");
pst.close();
        } catch (SQLException ex) {
            Logger.getLogger(ViewBlood.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

